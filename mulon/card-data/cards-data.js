// Helper function to get cards by rarity
export function getCardsByRarity(rarity) {
  return cardsData.filter(card => card.rarity === rarity);
}

// Helper function to get a random card with weights
export function getRandomCard(weights = rarityWeights) {
  const totalWeight = Object.values(weights).reduce((a, b) => a + b, 0);
  let random = Math.random() * totalWeight;
  
  for (const [rarity, weight] of Object.entries(weights)) {
    random -= weight;
    if (random <= 0) {
      const cardsOfRarity = getCardsByRarity(rarity);
      if (cardsOfRarity.length > 0) {
        return cardsOfRarity[Math.floor(Math.random() * cardsOfRarity.length)];
      }
    }
  }
  
  // Fallback
  return cardsData[Math.floor(Math.random() * cardsData.length)];
}

// Get card by card number
export function getCardByNumber(cardNumber) {
  return cardsData.find(card => card.cardNumber === cardNumber);
}

// Get card by id
export function getCardById(id) {
  return cardsData.find(card => card.id === id);
}

// Get card by index
export function getCardByIndex(index) {
  return cardsData[index] || null;
}
